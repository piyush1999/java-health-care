    private void loadReceptionistList() {
        try {
            Set<String> list = UserDao.getReceptionist().keySet();
            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No registered receptionist presennt");
                return;
            }
            list.forEach(jcReceptionistId::addItem);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    String pass;
    public boolean validateInput() {
        char[] pwd = txtPassword.getPassword();
        char[] repwd = txtRePassword.getPassword();
        if (pwd.length == 0 || repwd.length == 0) {
            JOptionPane.showMessageDialog(null, "Please enter both password field first", "Error", JOptionPane.ERROR_MESSAGE);
        }
        pass = String.valueOf(pwd);
        String repass = String.valueOf(repwd);
        if (!pass.equals(repass)) {
            JOptionPane.showMessageDialog(null, "Passwords Didn't Match", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

